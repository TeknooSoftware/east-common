declare(strict_types=1);

namespace Teknoo\East\Common\Doctrine\Filter\ODM;

use Doctrine\ODM\MongoDB\Mapping\ClassMetadata;
use Doctrine\ODM\MongoDB\Query\Filter\BsonFilter;
use Teknoo\East\Common\Contracts\Object\DeletableInterface;

/**
 * Filter to implement Soft Deletable feature in East based projects
 *
 * @copyright   Copyright (c) EIRL Richard Déloge (https://deloge.io - richard@deloge.io)
 * @copyright   Copyright (c) SASU Teknoo Software (https://teknoo.software - contact@teknoo.software)
 * @license     http://teknoo.software/license/bsd-3         3-Clause BSD License
 * @author      Richard Déloge <richard@teknoo.software>
 */
class SoftDeletableFilter extends BsonFilter
{
    public function addFilterCriteria(ClassMetadata $targetDocument): array
    {
        if (!$targetDocument->getReflectionClass()->implementsInterface(DeletableInterface::class)) {
            return [];
        }

        return ['deletedAt' => null];
    }
}
